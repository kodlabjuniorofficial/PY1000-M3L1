# Python'da metinleri (string) sayılarla çarpabiliriz. Bu bize harika bir güç verir!
print("Tek satırda 10 yıldız: " + "*" * 10)

# Şimdi bu gücü 'for' döngüsüyle birleştirelim ve 'i' değişkenini kullanalım.
print(" 'i' kullanarak üçgen çizimi ---")
for i in range(6):  # i sırasıyla 0, 1, 2, 3, 4, 5 olacak
    print("*" * i)

