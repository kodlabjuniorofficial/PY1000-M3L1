#  'for i in range(...)' satırındaki 'i', o an kaçıncı adımda olduğumuzu tutan sihirli bir kutu gibidir.
# range() ise bu kutunun hangi sayılarla dolacağını belirler.
# range(başlangıç, bitiş, adım_sayısı)
print("--- range(0, 50, 10) kullanımı ---")
for i in range(0, 50, 10):
    print("Şimdiki 'i' kutusunun değeri:", i)

